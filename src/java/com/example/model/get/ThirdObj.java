
package com.example.model.get;

public class ThirdObj {
    
    private String city;
    private int year;
    private int area;

    public void setCity(String city) {
        this.city = city;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public String getCity() {
        return city;
    }

    public int getYear() {
        return year;
    }

    public int getArea() {
        return area;
    }
    
}
